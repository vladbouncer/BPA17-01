public class Orange {
	public static int d,j;
	public static int org( int N[],int M[]) {
	int t =Fruits.t;
	int s =Fruits.s;
	d=0;//апельсины
	for (int i=0; i<Fruits.per1;i++)
	{
		if (N[i]>=t)
		{
			d=d+1;
		}
	}
j=0;//€блоки
Fruits.d=d;
	for (int i=0; i<Fruits.per2;i++)
	{
		if (M[i]<=s)
		{
			j=j+1;
		}
	}
	
	Fruits.j=j;
	return(d);
	}
	
}
